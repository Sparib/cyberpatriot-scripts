def handleUsers():
    user_file = open(prependHome("Desktop/users.txt"), 'r')
    wanted_users: list[str] = []
    for line in user_file:
        user = line.strip()
        wanted_users.append(user)
    
    passwd_file = open("/etc/passwd", 'r')
    current_users: list[str] = []
    for line in passwd_file:
        parts = line.split(":")
        user = parts[0].strip()
        uid = int(parts[2])
        if uid >= 1000 and uid < 65534 and user != "nobody": current_users.append(user)

    user_file.close()
    passwd_file.close()

    needed_users = [x for x in wanted_users if x not in current_users]
    bad_users = [x for x in current_users if x not in wanted_users]

    print("Needed users: {}".format(needed_users))
    print("Bad users: {}".format(bad_users))
    print("Commands:")
    for user in bad_users:
        removeUser(user)
    for user in needed_users:
        print("useradd", "-d", user)

def removeUser(user: str):
    if user == current_logged_in_user: return
    execute("deluser", "--remove-all-files", user)


def execute(command: str, *args: str) -> Union[str, None]:
    """Executes a command and passed arguments, first asking if the command should be run

    Args:
        command (str): Path to (or name of) executable

    Returns:
        [str | None]: Output of command or None if command is not run
    """
    cmd = [command, *args]
    print(*cmd)
    while 1:
        resp = input("Run command? (Y/n) ")
        if resp == "" or resp.lower() == "y": break # Breaks from while loop    (will run command)
        elif resp.lower() == "n": return None       # Returns from function     (will not run command)
        # No "else" which causes loop and asking question again if answer is unknown (aka not blank or y/n)
        
    proc = subprocess.Popen(cmd, stdout=subprocess.PIPE)

    out, _ = proc.communicate() # Takes output from function and returns it

    return out.decode('ascii')


def prependHome(path: str):    
    return os.path.join(os.path.expanduser("~{}".format(current_logged_in_user)), path)
    
